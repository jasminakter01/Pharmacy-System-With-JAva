 void shows(){
        try {
            DefaultTableModel dt=(DefaultTableModel) jTable1.getModel();
               dt.setRowCount(0);
            contest();
            String select="SELECT* FROM `pharmacy_management`.`supplier_info`";
            st = ct.prepareCall(select);
            rs = st.executeQuery(select);
            while(rs.next()){
              dt.addRow(new Object[]{rs.getInt(1),rs.getString(2),rs.getString(3),rs.getDate(4)});
            }
        } catch (SQLException ex) {
            Logger.getLogger(Medicine_Info.class.getName()).log(Level.SEVERE, null, ex);
        }

}
