void save(){
        if(jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Supplier Nameis Empty");
         
            jTextField1.requestFocus();
        }else if(jTextField2.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Mobile Number is Empty");
            jTextField2.requestFocus();
        }else{ try {
            contest();
            String select="SELECT* FROM `pharmacy_management`.`supplier_info` WHERE `name`='"+jTextField1.getText()+"'";
            st = ct.prepareCall(select);
            rs = st.executeQuery(select);
            if(rs.next()){
                JOptionPane.showMessageDialog(rootPane, "Sorry This Name Allready Save....");
            }else{
                  Date dt = new Date();
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
       
                String s="INSERT INTO `pharmacy_management`.`supplier_info`(`name`,`mobile`,`date`)VALUES (?,?,?);";
                ps=ct.prepareCall(s);
                ps.setString(1,jTextField1.getText());
                ps.setString(2,jTextField2.getText());
            ps.setString(3,df.format(dt));
                int m=ps.executeUpdate();
                if(m>0){
                    JOptionPane.showMessageDialog(rootPane, "Information Save.....");
                    shows();
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField1.requestFocus();
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Information  Save Failed.....");
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Medicine_Info.class.getName()).log(Level.SEVERE, null, ex);
        }

        }

}
    private void jLabel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel5MouseClicked
        if(jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Name * Weight is Empty");
            jTextField1.requestFocus();
        }else if(jTextField2.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Mobile Number is Empty");
            jTextField2.requestFocus();
        }else{ try {
            contest();
            String select="SELECT `name` FROM `pharmacy_management`.`supplier_info` WHERE `name`='"+jTextField1.getText()+"'";
            st = ct.prepareCall(select);
            rs = st.executeQuery(select);
            if(rs.next()){
                String s="UPDATE `pharmacy_management`.`supplier_info` SET`mobile` = '"+jTextField2.getText()+"'WHERE `name` = '"+jTextField1.getText()+"'";
                ps = ct.prepareCall(s);
                int m=ps.executeUpdate();
                if(m>0){
                    JOptionPane.showMessageDialog(rootPane, "Information Update.....");
                    shows();
                    jTextField1.setText("");
                        jTextField2.setText("");
                            jTextField1.requestFocus();
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Information  Update Failed.....");
                }

            }else{
                JOptionPane.showMessageDialog(rootPane, "Sorry This Name don't Save....");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Medicine_Info.class.getName()).log(Level.SEVERE, null, ex);
        }

        }

    }//GEN-LAST:event_jLabel5MouseClicked

